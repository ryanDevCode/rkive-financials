            <div class="card">
                <div class="card-header">
                    <h5>Employee Financial Data Handling Policy</h5>
                </div>
                <div class="card-body">
                    <div class="row">
                        <div class="col-sm-12">
                            <h6>1. Purpose</h6>
                            <p>This policy outlines the responsibilities of employees when handling and accessing
                                company financial data, including accounting statements and other financial information.
                            </p>

                            <h6>2. Scope</h6>
                            <p>This policy applies to all employees who have access to or handle company financial data.
                            </p>

                            <h6>3. Confidentiality</h6>
                            <p>Employees must maintain the confidentiality of all company financial data. This includes
                                not sharing financial information with unauthorized individuals or using it for personal
                                gain.</p>

                            <h6>4. Access</h6>
                            <p>Access to company financial data should be limited to employees who require it to perform
                                their job duties. Any unauthorized access or use of financial data is prohibited.</p>

                            <h6>5. Data Integrity</h6>
                            <p>Employees must ensure the integrity of company financial data by accurately recording and
                                reporting financial information. Any discrepancies or errors should be reported
                                promptly.</p>

                            <h6>6. Compliance</h6>
                            <p>Employees must comply with all applicable laws, regulations, and company policies
                                regarding the handling and protection of financial data.</p>

                            <h6>7. Security</h6>
                            <p>Employees should take all necessary measures to protect company financial data from
                                unauthorized access, including using secure passwords and encryption where appropriate.
                            </p>

                            <h6>8. Training</h6>
                            <p>Employees should receive training on how to handle company financial data responsibly and
                                securely.</p>

                            <h6>9. Reporting</h6>
                            <p>Employees should report any suspected breaches of this policy or incidents involving the
                                unauthorized access or use of financial data.</p>

                            <h6>10. Enforcement</h6>
                            <p>Violation of this policy may result in disciplinary action, up to and including
                                termination of employment.</p>

                            <h6>11. Acknowledgement</h6>
                            <p>By accessing company financial data, employees acknowledge that they have read,
                                understood, and agree to comply with this policy.</p>

                            <h6>12. Review</h6>
                            <p>This policy will be reviewed periodically and updated as necessary to ensure its
                                effectiveness and compliance with applicable laws and regulations.</p>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    </div>
</div>
