                    <div class="row">
                        <div class="col-sm-12">
                            <h6>Data Security:</h6>
                            <p> The company is responsible for implementing and maintaining appropriate security
                                measures to protect
                                financial data from unauthorized access, including encryption, access controls, and
                                regular security
                                audits.</p>

                            <h6>Access Control:</h6>
                            <p> The company should ensure that access to financial data is limited to authorized
                                personnel only and that
                                employees receive appropriate training on data handling procedures.</p>

                            <h6>Compliance:</h6>
                            <p> The company is responsible for ensuring that all employees are aware of and comply with
                                relevant laws,
                                regulations, and company policies regarding the handling of financial data.</p>

                            <h6>Data Integrity:</h6>
                            <p> The company should establish procedures to ensure the accuracy and integrity of
                                financial data,
                                including regular audits and checks for discrepancies.</p>

                            <h6>Confidentiality:</h6>
                            <p> The company must maintain the confidentiality of financial data and ensure that it is
                                not disclosed to
                                unauthorized individuals or used for unauthorized purposes.</p>

                            <h6>Reporting:</h6>
                            <p> The company should establish a process for employees to report any suspected breaches of
                                data security
                                or incidents involving the unauthorized access or use of financial data.</p>

                            <h6>Training:</h6>
                            <p> The company should provide regular training to employees on data handling best practices
                                and the
                                importance of data security.</p>

                            <h6>Compliance Monitoring:</h6>
                            <p> The company should regularly monitor and review its data handling practices to ensure
                                compliance with
                                this policy and relevant laws and regulations.</p>

                            <h6>Cooperation with Authorities:</h6>
                            <p> In the event of a data breach or other security incident involving financial data, the
                                company should
                                cooperate fully with authorities and provide any necessary assistance in the
                                investigation.</p>

                            <h6>Review and Update:</h6>
                            <p> The company should regularly review and update this policy to ensure its effectiveness
                                and compliance
                                with evolving laws and regulations.</p>
